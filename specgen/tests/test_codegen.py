def test_use_constants_cpp_compiles_and_runs(tmp_path):
    """examples/use_constants.cpp must compile and produce the expected output."""
    if not shutil.which("g++"):
        pytest.skip("g++ not in PATH")

    # Regenerate packet header
    run_codegen("--target", "cpp", "--domain", "packet", "--out", str(INCLUDE_DIR))

    exe = tmp_path / "use_constants_test.exe"
    src = SPECGEN_ROOT / "examples" / "use_constants.cpp"

    compile_result = subprocess.run(
        ["g++", "-std=c++17",
         "-I", str(INCLUDE_DIR),
         str(src),
         "-o", str(exe)],
        capture_output=True, text=True,
    )
    assert compile_result.returncode == 0, (
        f"Compilation failed:\n{compile_result.stderr}"
    )

    run_result = subprocess.run(
        [str(exe)], capture_output=True, text=True,
    )
    assert run_result.returncode == 0, run_result.stderr
    # All optional fields disabled: axi_ch is the first enabled field at LSB 0,
    # so packed hex of (axi_ch=2, src_id=5, dst_id=0x12, last=1, rob_req=1,
    # rob_idx=7) is the minimal-header value.
    assert "0x0000000007C0902A" in run_result.stdout, (
        f"Expected header value not found in output:\n{run_result.stdout}"
    )


