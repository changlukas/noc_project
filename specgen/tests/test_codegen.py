def test_check_mode_exits_zero_when_clean():
    """--check must exit 0 when committed headers match fresh regen."""
    # First ensure headers are fresh
    for domain in ("packet", "signals", "params"):
        run_codegen("--target", "cpp", "--domain", domain, "--out", str(INCLUDE_DIR))
    r = run_codegen("--check")
    assert r.returncode == 0, (
        f"--check should exit 0 when headers are fresh.\n"
        f"stdout: {r.stdout}\nstderr: {r.stderr}"
    )


def test_check_mode_exits_one_on_drift(tmp_path):
    """--check must exit 1 when a committed header has been modified."""
    # First ensure headers are fresh
    for domain in ("packet", "signals", "params"):
        run_codegen("--target", "cpp", "--domain", domain, "--out", str(INCLUDE_DIR))

    target_header = INCLUDE_DIR / "ni_flit_constants.h"
    original_text = target_header.read_text(encoding="ascii")

    try:
        # Inject a fake drift line
        drifted = original_text + "\n// DRIFT INJECTED FOR TEST\n"
        target_header.write_text(drifted, encoding="ascii")

        r = run_codegen("--check")
        assert r.returncode == 1, (
            "--check should exit 1 when a header has drifted"
        )
    finally:
        # Restore the header regardless of test outcome
        target_header.write_text(original_text, encoding="ascii")


