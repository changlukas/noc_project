class TestSvPacketEmit:
    def setup_method(self):
        r = run_codegen("--target", "sv", "--domain", "packet",
                        "--out", str(RTL_PKG_DIR))
        assert r.returncode == 0, r.stderr

    def test_exits_zero(self):
        r = run_codegen("--target", "sv", "--domain", "packet",
                        "--out", str(RTL_PKG_DIR))
        assert r.returncode == 0, r.stderr

    def test_file_exists(self):
        assert (RTL_PKG_DIR / "ni_flit_pkg.sv").exists()

    def test_package_structure(self):
        text = _sv_text("ni_flit_pkg.sv")
        assert "package ni_flit_pkg;" in text
        assert "endpackage" in text

    def test_localparam_int_unsigned(self):
        text = _sv_text("ni_flit_pkg.sv")
        assert "localparam int unsigned" in text

    def test_flit_width_constant(self):
        text = _sv_text("ni_flit_pkg.sv")
        assert "FLIT_WIDTH" in text

    def test_header_field_lsb_msb(self):
        text = _sv_text("ni_flit_pkg.sv")
        assert "ROB_IDX_LSB" in text
        assert "ROB_IDX_MSB" in text

    def test_ifndef_include_guard(self):
        text = _sv_text("ni_flit_pkg.sv")
        assert "`ifndef NI_FLIT_PKG_SVH" in text
        assert "`define NI_FLIT_PKG_SVH" in text
        assert "`endif" in text

    def test_provenance_banner(self):
        text = _sv_text("ni_flit_pkg.sv")
        assert "AUTO-GENERATED by tools/codegen.py" in text
        assert "Source SHA:" in text
        assert "Generator version:" in text
        assert "Generated at:" in text

    def test_enabled_localparams_present(self):
        """ni_flit_pkg.sv must contain localparam bit _ENABLED constants."""
        text = _sv_text("ni_flit_pkg.sv")
        assert "localparam bit" in text
        assert "_ENABLED" in text

    def test_padding_fields_enabled_zero(self):
        """Fields declared as padding must emit ENABLED = 1'b0 in SV package.

        Post fixed-56b refactor: only the synthetic ``rsvd`` field is padding.
        """
        text = _sv_text("ni_flit_pkg.sv")
        assert "localparam bit          RSVD_ENABLED = 1'b0;" in text, (
            "Expected RSVD_ENABLED = 1'b0; not found in ni_flit_pkg.sv"
        )

    def test_functional_fields_enabled_one(self):
        """Functional fields must emit ENABLED = 1'b1 in SV package."""
        text = _sv_text("ni_flit_pkg.sv")
        # axi_ch is MUST functional (AXI channel routing)
        for field in ("AXI_CH", "SRC_ID", "DST_ID", "VC_ID", "LAST", "ROB_REQ", "ROB_IDX"):
            assert f"localparam bit          {field}_ENABLED = 1'b1;" in text, (
                f"Expected {field}_ENABLED = 1'b1; not found in ni_flit_pkg.sv"
            )

    def test_payload_field_positions_emitted(self):
        """ni_flit_pkg.sv must contain per-channel-prefixed payload field constants."""
        text = _sv_text("ni_flit_pkg.sv")
        # Representative constants from each channel
        for sig in ("AW_AWID_LSB", "AR_ARID_LSB", "W_WDATA_LSB",
                    "B_BID_LSB", "R_RDATA_LSB"):
            assert f"localparam int unsigned {sig}" in text, (
                f"missing {sig} in ni_flit_pkg.sv"
            )


