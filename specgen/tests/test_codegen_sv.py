"""Codegen SV emitter tests -- Task 8.

Tests cover:
- --target sv --domain {packet|signals|params} each produces a .sv file.
- Each .sv file has the correct package / endpackage structure.
- localparam int unsigned present in files.
- Provenance banner (AUTO-GENERATED, Source SHA, Generator version, Generated at).
- ifndef/define/endif include guard in files.
- --check mode covers SV files (detects drift, passes when clean).
- --lint-sv skips gracefully when verilator is not in PATH.
"""
from __future__ import annotations
import shutil
import subprocess
import sys
from pathlib import Path

import pytest

SPECGEN_ROOT = Path(__file__).resolve().parent.parent
CODEGEN = SPECGEN_ROOT / "tools" / "codegen.py"
RTL_PKG_DIR = SPECGEN_ROOT / "generated" / "sv"


def run_codegen(*args: str) -> subprocess.CompletedProcess:
    return subprocess.run(
        [sys.executable, str(CODEGEN), *args],
        capture_output=True,
        text=True,
        cwd=str(SPECGEN_ROOT),
    )


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _sv_text(filename: str) -> str:
    """Read committed SV file (regenerate first to ensure it exists)."""
    path = RTL_PKG_DIR / filename
    if not path.exists():
        pytest.skip(f"{filename} not found; run --target sv first")
    return path.read_text(encoding="ascii")


