class TestLintSv:
    def test_lint_sv_graceful_skip_or_pass(self):
        """--lint-sv must exit 0 (either skip or lint pass; never crash)."""
        # First ensure SV files exist.
        for domain in ("packet", "signals", "params"):
            run_codegen("--target", "sv", "--domain", domain, "--out", str(RTL_PKG_DIR))

        r = run_codegen("--lint-sv")
        # Either 0 (skip or pass) or non-zero only if verilator found a real lint error.
        # Since we can't guarantee verilator outcome on all CI envs, we just confirm
        # it doesn't crash with an exception (non-zero return from subprocess == lint warning).
        # The requirement is that absence of verilator exits 0, not that lint is perfect.
        if shutil.which("verilator") is None:
            assert r.returncode == 0, (
                f"--lint-sv must exit 0 when verilator not in PATH.\n"
                f"stderr: {r.stderr}"
            )
            assert "skip" in r.stderr.lower(), (
                f"Expected [skip] message in stderr when verilator absent.\n"
                f"stderr: {r.stderr}"
            )
        # If verilator IS present, returncode may be 0 or non-zero depending on
        # lint warnings -- we just verify the command ran without Python exceptions.
        assert "Traceback" not in r.stderr, (
            f"--lint-sv raised a Python exception:\n{r.stderr}"
        )

    def test_lint_sv_no_python_exception_without_verilator(self, monkeypatch):
        """Simulate verilator missing by overriding PATH, confirm graceful skip."""
        import os
        env = os.environ.copy()
        env["PATH"] = ""  # empty PATH -> shutil.which("verilator") returns None
        result = subprocess.run(
            [sys.executable, str(CODEGEN), "--lint-sv"],
            capture_output=True,
            text=True,
            env=env,
            cwd=str(SPECGEN_ROOT),
        )
        assert result.returncode == 0, f"Expected exit 0, got {result.returncode}.\nstderr: {result.stderr}"
        assert "skip" in result.stderr.lower(), (
            f"Expected skip message.\nstderr: {result.stderr}"
        )



